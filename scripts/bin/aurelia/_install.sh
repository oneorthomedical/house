function isAuInstalled() {
    if [[ ! $(which au;) ]]
    then
        echo "false";
    else
        echo "true";
    fi
}

function auInstall() {
    groupLog "auInstall";
    installed=$(isAuInstalled;);
    if [[ "${installed}" == "false" ]]
    then
      ./fiddle.sh "setup" "${OS}" "au";
    fi
}
